package com.phincon.talents.app.dto;

import java.util.Date;



public class AttendanceRefDTO {
	private Date date;
	private int sequenceDay;
	private int sequencePattern;
	
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public int getSequenceDay() {
		return sequenceDay;
	}
	public void setSequenceDay(int sequenceDay) {
		this.sequenceDay = sequenceDay;
	}
	public int getSequencePattern() {
		return sequencePattern;
	}
	public void setSequencePattern(int sequencePattern) {
		this.sequencePattern = sequencePattern;
	}
	
	
	
	

}
